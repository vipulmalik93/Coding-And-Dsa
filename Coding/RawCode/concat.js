// In JavaScript, concat() is a method used to combine two or more arrays or strings.

// Example1:
let array1 = [1, 2, 3];
let array2 = [4, 5, 6];
let combinedArray = array1.concat(array2);

console.log(combinedArray); 


// Example2: 
let string1 = "Hello";
let string2 = " World!";
let combinedString = string1.concat(string2);

console.log(combinedString);